function module.menu_configuration_section()
    imgui.Spacing()
    imgui.Text("Check Boxes")
    imgui.Separator()
    imgui.Spacing()
    fcommon.ValueSwitch({ name = "Lock player in menu",var = tmenu.lock_player,help_text = "Player control gets locked when Cheat-Menu is open."})
    fcommon.ValueSwitch({ name = "Render Cheat-Menu in pause menu",var = tmenu.render_in_menu,help_text = "Cheat Menu gets shown even in pause menu."})
    fcommon.ValueSwitch({ name = "Auto reload Cheat-Menu",var = cheat_menu.auto_reload,help_text = "Automatically reloads script if the script gets terminated/crashed.(might cause issues)"})
    if imgui.BeginMenu("Style Editor",true) then
        imgui.Spacing()
        imgui.Text("Style Editor")
        imgui.Separator()
        imgui.Spacing()
        imgui.ShowStyleEditor(imgui.GetStyle())
        imgui.EndMenu()
    end
end
